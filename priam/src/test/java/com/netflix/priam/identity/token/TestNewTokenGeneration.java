    public TestNewTokenGeneration(
        String region, int racSize, int regionCount, boolean anotherInstanceInRac, int idSupplement, boolean myRacInRacs, boolean useMurmur3, String correctToken) {
        String rac = region + "a";
        InstanceInfo instanceInfo = new FakeInstanceInfo("foo", rac, region);
        FakeConfiguration config = new FakeConfiguration();
        config.setFakeConfig("Priam.zones.available", getRacs(regionCount, myRacInRacs));
        if (useMurmur3) {
            config.setFakeConfig("Priam.partitioner", "org.apache.cassandra.dht.Murmur3Partitioner");
        }
        FakeMembership membership = new FakeMembership();
        membership.setRacCount(regionCount * 3);
        membership.setRacSize(racSize);
        FakePriamInstanceFactory factory = new FakePriamInstanceFactory(instanceInfo);
        ITokenManager tokenManager = new TokenManager(config);
        Sleeper sleeper = new FakeSleeper();
        tokenRetriever = new NewTokenRetriever(factory, membership, config, sleeper, tokenManager, instanceInfo);
        if (anotherInstanceInRac) {
            PriamInstance pi = new PriamInstance();
            pi.setRac(rac);
            pi.setId(tokenManager.regionOffset(region) + idSupplement);
            factory.create("", tokenManager.regionOffset(region) + idSupplement, "", "", "", rac, null, "");
        }
        ListMultimap<String, PriamInstance> locMap =
            Multimaps.newListMultimap(new HashMap<>(), Lists::newArrayList);
        for (PriamInstance ins : factory.getAllIds(config.getAppName())) {
            locMap.put(ins.getRac(), ins);
        }
        tokenRetriever.setLocMap(locMap);
        logPrefix = joiner.join("\"" + region + "\"", racSize, regionCount, anotherInstanceInRac, idSupplement, myRacInRacs, useMurmur3);
        this.correctToken = correctToken;
    }

    ImmutableList<String> getRacs(int racCount, boolean myRacInRacs) {
        Preconditions.checkArgument(racCount > 0 && racCount < 4);
        char suffix = 'a';
        if (!myRacInRacs) suffix++;
        ImmutableList.Builder<String> racs = ImmutableList.builder();
        for (int ii = 0; ii < 3; ii++) {
            racs.add("us-east-1" + (char)(suffix + ii));
        }
        if (racCount > 1) {
            for (int ii = 0; ii < 3; ii++) {
                racs.add("eu-west-1" + (char)(suffix + ii));
            }
        }
        if (racCount > 2) {
            for (int ii = 0; ii < 3; ii++) {
                racs.add("us-west-2" + (char)(suffix + ii));
            }
        }
        return racs.build();
    }

